        DefaultTableModel t=(DefaultTableModel) T1.getModel();
        t.setRowCount(0);
        int i=Integer.parseInt(sp.getValue().toString());
        int a=0;
        try{
            Class.forName("java.sql.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/abcd","root","sps");
            Statement stmt=con.createStatement();
            String query= "Select * from payment_detail where Login_Id='"+Id+"' ORDER BY Payment_no DESC;" ;

            ResultSet rs=stmt.executeQuery(query);
            while(rs.next()) {
                t.addRow(new Object[]{rs.getInt(1), rs.getString(3), rs.getDouble(4),
                rs.getString(5)});
                a++;
                if(a>=i){
                    break;
                }
            }
        }

        catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
